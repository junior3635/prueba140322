    {
        $productos_invoices = Product::where('invoice_id',$product['invoice_id'])->get();
        $total = 0;
        foreach ($productos_invoices as $key) {
            $total = $total + ($key['price']*$key['quantity']);
        }

        $invoice = Invoice::where('id',$product['invoice_id'])->update([
            'total' => $total
        ]);
    }

